char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int f;

	i = 0;
	f = 0;
	if (to_find[f] == '\0')
	{
		return (str);
	}
	while (str[i])
	{
		while (str[i + f] == to_find[f])
		{
			f++;
			if (to_find[f] == '\0')
			{
				return (&str[i]);
			}
		}
		f = 0;
		i++;
	}
	return (0);
}
