#include <unistd.h>

char g_i;
char g_j;
char g_k;

void	ft_print_comb(void)
{
	g_i = '0' - 1;
	while (g_i <= '6')
	{
		g_i++;
		g_j = g_i;
		while (g_j <= '7')
		{
			g_j++;
			g_k = g_j;
			while (g_k <= '8')
			{
				g_k++;
				write(1, &g_i, 1);
				write(1, &g_j, 1);
				write(1, &g_k, 1);
				if (!(g_i == '7' && g_j == '8' && g_k == '9'))
				{
					write(1, ", ", 2);
				}
			}
		}
	}
}
