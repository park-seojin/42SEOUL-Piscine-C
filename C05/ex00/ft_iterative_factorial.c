int		ft_iterative_factorial(int nb)
{
	int fac;
	int i;

	fac = 1;
	i = 1;
	if (nb < 0)
		return (0);
	while (i <= nb)
	{
		fac = fac * i;
		i++;
	}
	return (fac);
}
